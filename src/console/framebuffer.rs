const CHAR_WIDTH: usize = 8;
const CHAR_HEIGHT: usize = 8;

pub struct FrameBufferWriter {
    buffer: &'static mut [u8],
    info: FrameBufferInfo,
    col: usize,
    row: usize,
}

impl FrameBufferWriter {
    pub fn new(buffer: &'static mut [u8], info: FrameBufferInfo) -> Self {
        Self { buffer, info, col: 0, row: 0 }
    }

    fn width_in_chars(&self) -> usize {
        self.info.width as usize / CHAR_WIDTH
    }

    fn height_in_chars(&self) -> usize {
        self.info.height as usize / CHAR_HEIGHT
    }

    fn write_pixel(&mut self, x: usize, y: usize, r: u8, g: u8, b: u8) {
        let bpp = self.info.bytes_per_pixel as usize;
        let stride = self.info.stride as usize;
        let offset = y * stride * bpp + x * bpp;
        if offset + bpp > self.buffer.len() {
            return;
        }
        match self.info.pixel_format {
            PixelFormat::Rgb => {
                self.buffer[offset] = r;
                self.buffer[offset + 1] = g;
                self.buffer[offset + 2] = b;
            }
            PixelFormat::Bgr => {
                self.buffer[offset] = b;
                self.buffer[offset + 1] = g;
                self.buffer[offset + 2] = r;
            }
            _ => {
                self.buffer[offset] = (r as u16 + g as u16 + b as u16) as u8 / 3;
            }
        }
    }

    fn write_char(&mut self, c: char) {
        match c {
            '\n' => self.new_line(),
            '\t' => {
                for _ in 0..4 { self.write_char(' '); }
            }
            c => {
                if self.col >= self.width_in_chars() {
                    self.new_line();
                }
                let idx = c as usize;
                let glyph = if idx >= 0x20 && idx <= 0x7e {
                    &FONT[idx - 0x20]
                } else {
                    &FONT[0]  // space
                };
                let px = self.col * CHAR_WIDTH;
                let py = self.row * CHAR_HEIGHT;
                for (row, &bits) in glyph.iter().enumerate() {
                    for col in 0..8 {
                        let set = (bits >> (7 - col)) & 1 != 0;
                        let (r, g, b) = if set { (255, 255, 0) } else { (0, 0, 0) };
                        self.write_pixel(px + col, py + row, r, g, b);
                    }
                }
                self.col += 1;
            }
        }
    }

    fn new_line(&mut self) {
        self.col = 0;
        self.row += 1;
        if self.row >= self.height_in_chars() {
            self.scroll();
        }
    }

    fn scroll(&mut self) {
        let bpp = self.info.bytes_per_pixel as usize;
        let stride = self.info.stride as usize;
        let row_bytes = stride * bpp * CHAR_HEIGHT;
        let total = stride * bpp * self.info.height as usize;
        self.buffer.copy_within(row_bytes..total, 0);
        let clear_start = total - row_bytes;
        self.buffer[clear_start..total].fill(0);
        self.row = self.height_in_chars() - 1;
    }

    pub fn write_string(&mut self, s: &str) {
        for c in s.chars() {
            self.write_char(c);
        }
    }
}

impl fmt::Write for FrameBufferWriter {
    fn write_str(&mut self, s: &str) -> fmt::Result {
        self.write_string(s);
        Ok(())
    }
}

static FB_WRITER: spin::Mutex<Option<FrameBufferWriter>> = spin::Mutex::new(None);

pub fn init(framebuffer: &'static mut FrameBuffer) {
    let info = framebuffer.info();
    let buffer = framebuffer.buffer_mut();
    *FB_WRITER.lock() = Some(FrameBufferWriter::new(buffer, info));
}

#[doc(hidden)]
pub fn _print(args: fmt::Arguments) {
    use core::fmt::Write;
    use x86_64::instructions::interrupts;
    interrupts::without_interrupts(|| {
        if let Some(writer) = FB_WRITER.lock().as_mut() {
            writer.write_fmt(args).unwrap();
        }
    });
}
